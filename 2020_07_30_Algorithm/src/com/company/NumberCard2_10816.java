package com.company;

import java.util.Scanner;

public class NumberCard2_10816 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);

        int N = sc.nextInt();
        int[] arrN = new int[N];
        for (int i = 0; i < N; i++){
            arrN[i] = sc.nextInt();
        }
    }
}
